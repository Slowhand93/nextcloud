# nextcloud-docker

This nextcloud docker image includes php-fpm, nginx+ssl, letsencrypt. By default it uses sqllite but can be configured to use external Myql or PostgreSQL database.

It is a fork of https://github.com/Wonderfall/dockerfiles/tree/master/nextcloud/11.0 pluss few additional modifications like SSL and letsencrypt support from https://github.com/ngineered/nginx-php-fpm

